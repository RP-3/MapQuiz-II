import UIKit

class UIConstants {

    public static func amatic(size: CGFloat) -> UIFont {
        return UIFont(name: "AmaticSC-Regular", size: size)!
    }

    public static func amaticBold(size: CGFloat) -> UIFont {
        return UIFont(name: "AmaticSC-Bold", size: size)!
    }
}
